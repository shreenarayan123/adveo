// ─── Core Generation (single function, all paths) ─────────────────────────────

async function generateAdConceptRaw(
  productName: string,
  theme: string,
  targetAudience: string,
  category?: string,
  patterns?: WinningPattern,
  features?: string,
  imageAnalysis?: ProductImageAnalysis,
  narrativeStyle?: NarrativeStyle,
): Promise<AdConcept> {
  let lastErr: unknown = null;

  // Auto-detect narrative style if not explicitly provided
  const resolvedStyle: NarrativeStyle = narrativeStyle
    ?? detectNarrativeStyle(productName, features ?? '', imageAnalysis?.productCategory);

  console.log(`[AI] Narrative style: ${resolvedStyle} (product: ${productName})`);

  // Category 2 sensory products use a strict 3-step prompt chain:
  // 1) product analysis -> 2) video brief generation -> 3) quality gate.
  if (resolvedStyle === 'sensory') {
    const productDescription = [
      productName,
      features?.trim() ? `Features: ${features}` : null,
      imageAnalysis?.productCategory ? `Category hint: ${imageAnalysis.productCategory}` : null,
      targetAudience?.trim() ? `Target audience: ${targetAudience}` : null,
      theme?.trim() ? `Theme/Tone: ${theme}` : null,
    ]
      .filter(Boolean)
      .join('. ');

    const chainResult = await runCategory2PromptChain(productDescription);
    return buildCategory2ConceptFromChain(productName, targetAudience, chainResult);
  }

  for (let attempt = 1; attempt <= 3; attempt++) {
    try {
      const res = await openai.chat.completions.create({
        model: 'gpt-4o',                          // consistent across all paths
        temperature: 1.0,
        max_tokens: 2000,
        response_format: { type: 'json_object' },
        messages: [
          { role: 'system', content: buildSystemPrompt(category, patterns, resolvedStyle) },
          { role: 'user', content: buildUserPrompt(productName, theme, targetAudience, features, imageAnalysis, resolvedStyle) },
        ],
      });

      const raw = res.choices[0]?.message?.content ?? '{}';
      const parsed = JSON.parse(raw);
      const normalized = normalizeAdConceptCandidate(parsed, productName, resolvedStyle);
      return AdConceptSchema.parse(normalized);
    } catch (err) {
      lastErr = err;
      console.error(`Ad concept gen failed (attempt ${attempt}/3):`, err);
      await new Promise(r => setTimeout(r, 500 * attempt));
    }
  }

  throw new Error(`Failed to generate ad concept: ${(lastErr as Error)?.message ?? lastErr}`);
}


