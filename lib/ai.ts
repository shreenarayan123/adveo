async function runCategory2PromptChain(productDescription: string): Promise<{
  productVars: Category2ProductAnalysis;
  finalPrompt: string;
  qcReport: Category2QualityGateResult;
}> {
  const analysisResponse = await openai.chat.completions.create({
    model: 'gpt-4o',
    max_tokens: 1000,
    response_format: { type: 'json_object' },
    messages: [
      { role: 'system', content: buildCategory2AnalysisSystemPrompt() },
      { role: 'user', content: productDescription },
    ],
  });

  const rawAnalysis = stripJsonCodeFences(analysisResponse.choices[0]?.message?.content ?? '{}');
  const productVars = Category2ProductAnalysisSchema.parse(JSON.parse(rawAnalysis));

  const briefResponse = await openai.chat.completions.create({
    model: 'gpt-4o',
    max_tokens: 2000,
    messages: [
      { role: 'system', content: buildCategory2VideoBriefSystemPrompt() },
      { role: 'user', content: buildCategory2VideoBriefUserPrompt(productVars) },
    ],
  });

  const generatedPrompt = (briefResponse.choices[0]?.message?.content ?? '').trim();

  const qcResponse = await openai.chat.completions.create({
    model: 'gpt-4o',
    max_tokens: 1000,
    response_format: { type: 'json_object' },
    messages: [
      { role: 'system', content: buildCategory2QualityGateSystemPrompt() },
      { role: 'user', content: generatedPrompt },
    ],
  });

  const rawQc = stripJsonCodeFences(qcResponse.choices[0]?.message?.content ?? '{}');
  const qcReport = Category2QualityGateSchema.parse(JSON.parse(rawQc));

  return {
    productVars,
    finalPrompt: qcReport.passes ? generatedPrompt : qcReport.fixed_prompt,
    qcReport,
  };
}

